  CREATE OR REPLACE EDITIONABLE FUNCTION "DB_KLUB"."NAJLEPSI_ZAWODNICY" RETURN SYS_REFCURSOR IS
    v_cursor SYS_REFCURSOR;
BEGIN
    OPEN v_cursor FOR
        SELECT 
