--  DDL for Function OBLICZ_BUDZET_DRUZYNY
--------------------------------------------------------

  CREATE OR REPLACE EDITIONABLE FUNCTION "DB_KLUB"."OBLICZ_BUDZET_DRUZYNY" (p_druzyna_id NUMBER) RETURN NUMBER IS
    v_suma_sponsorowania NUMBER;
    v_suma_finansow NUMBER;
    v_budzet NUMBER;
BEGIN
    -- Pobierz sumę kwot od sponsorów dla danej drużyny
    SELECT SUM(kwota_sponsorowania) INTO v_suma_sponsorowania
    FROM sponsorzy
    WHERE druzyna_id = p_druzyna_id;

    -- Pobierz sumę wydatków finansowych dla danej drużyny
    SELECT SUM(kwota) INTO v_suma_finansow
    FROM finanse
    WHERE druzyna_id = p_druzyna_id;

    -- Oblicz budżet drużyny
    v_budzet := NVL(v_suma_sponsorowania, 0) - NVL(v_suma_finansow, 0);

    RETURN v_budzet;
END;

